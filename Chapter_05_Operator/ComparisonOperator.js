5 == "5"    // true  (loose equality - compares value only)
5 === "5"   // false (strict equality - compares value + type)
5 != "5"    // false
5 !== "5"   // true
5 > 3       // true
5 < 3       // false
5 >= 5      // true
5 <= 4      // false

